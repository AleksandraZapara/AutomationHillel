package org.hillel.calculator;

public class Calculator {

    public int square(int a) {
        return a * a;
    }

    public int multiply(int a, int b, int c) {
        return a * b * c;
    }

    public int division(int a, int b){
        return a / b;
    }

    public int subtraction(int a, int b) {
        return a - b;
    }
}
